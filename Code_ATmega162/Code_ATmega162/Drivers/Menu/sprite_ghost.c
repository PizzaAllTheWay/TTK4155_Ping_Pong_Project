void sprite_ghost(){
	oled_clear_screen();
	// Ghost Animation Back and Forth <3
	uint8_t y_start = 1;
	uint8_t y_stop = 7;
	uint8_t speed_x = 7;
	uint8_t speed_y = 1;
	uint8_t direction_x = 0;
	
	uint8_t x_start = 32;
	oled_set_sprite(*sprite_ghost_binary, 4, 4, x_start, y_start);
	
	for (uint8_t i = 0; i < 100; i++) {
		// RIGHT
		direction_x = 1;
		oled_move_screen(
		y_start,
		y_stop,
		speed_x,
		speed_y,
		direction_x
		);
		_delay_ms(100);
		
		// LEFT
		direction_x = 0;
		oled_move_screen(
		y_start,
		y_stop,
		speed_x,
		speed_y,
		direction_x
		);
		_delay_ms(100);
	}
	//oled_clear_screen();
	return;
}